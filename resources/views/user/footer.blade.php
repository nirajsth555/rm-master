        //This functions sends an ajax request to server which pulls the results according to the page number supplied
        function getPosts(page) {
            $.ajax({
                url : '?page=' + page,
                dataType: 'json',
            }).done(function (data) {
                //After getting data replace the current contents of post-wrap div with newly arrived data
                $('.post-wrap').html(data);
                $("html, body").animate({ scrollTop: $('.page-title').offset().top - $('#header').height() });
            }).fail(function () {
                alert('Posts could not be loaded.');
            });
        }

        $(function(){
            //Trigger calling getPost on click of pagination link
            $(document).on('click', '.pagination a', function (e) {
                getPosts($(this).attr('href').split('page=')[1]);
                e.preventDefault();
            });
        });

        $('#ss').on('keyup',function(){
            //As the user types his search keyword, send request to the server to fetch news matching this keyword
            $value=$(this).val();
            //Send request only if there the keyword is not empty 
            if ($value){
                $.ajax({
     
                 type : 'get',
     
                 url : '{{URL::to('search')}}',
     
                 data:{'search':$value},
     
                 success:function(data){
                    //Reset the list of news to blank
                    $('.search-group').html('');
                    //If search is succesfull
                    if (data){
                        //For each news items create an li with link and append in to search-group ul in news.blade.php under search box
                        $.each(data,function(key,value){
                            var link = $('<a>').attr('href',"{{ url('/newsitem') }}/"+value.id).text(value.english_title);
                            var li = $('<li>').append(link);
                            $('.search-group').append(li);
                        });
                    }else{
                        //If no results are found reset the list to blank
                        var li = $('<li>').text('No results found').append(link);
                        $('.search-group').append(li);
                    }
      
                 }
     
                });
